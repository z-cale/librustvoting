// ValidateRoundForVoting checks that a vote round exists, has ACTIVE status,
// and has not expired (belt-and-suspenders: EndBlocker may not have run yet
// this block).
func (k Keeper) ValidateRoundForVoting(ctx context.Context, roundID []byte) error {
	kvStore := k.OpenKVStore(ctx)
	round, err := k.GetVoteRound(kvStore, roundID)
	if err != nil {
		return err // wraps ErrRoundNotFound if missing
	}

	if round.Status != types.SessionStatus_SESSION_STATUS_ACTIVE {
		return fmt.Errorf("%w: status is %s", types.ErrRoundNotActive, round.Status)
	}

	sdkCtx := sdk.UnwrapSDKContext(ctx)
	blockTime := uint64(sdkCtx.BlockTime().Unix())

	if blockTime >= round.VoteEndTime {
		return fmt.Errorf("%w: vote_end_time %d <= block_time %d", types.ErrRoundNotActive, round.VoteEndTime, blockTime)
	}

	return nil
}

// ValidateRoundForShares checks that a vote round exists and is in a state
// that accepts MsgRevealShare. Shares are accepted when the round is ACTIVE
// (with time check) or TALLYING (unconditionally).
func (k Keeper) ValidateRoundForShares(ctx context.Context, roundID []byte) error {
	kvStore := k.OpenKVStore(ctx)
	round, err := k.GetVoteRound(kvStore, roundID)
	if err != nil {
		return err
	}

	switch round.Status {
	case types.SessionStatus_SESSION_STATUS_ACTIVE:
		// Belt-and-suspenders: also check time in case EndBlocker hasn't run yet.
		sdkCtx := sdk.UnwrapSDKContext(ctx)
		blockTime := uint64(sdkCtx.BlockTime().Unix())
		if blockTime >= round.VoteEndTime {
			// Time has passed but EndBlocker hasn't transitioned yet — still
			// accept shares (the round will become TALLYING this block).
			return nil
		}
		return nil

	case types.SessionStatus_SESSION_STATUS_TALLYING:
		// Tallying phase: shares are accepted unconditionally.
		return nil

	default:
		return fmt.Errorf("%w: status is %s", types.ErrRoundNotActive, round.Status)
	}
}

// ValidateRoundForTally checks that a vote round exists and is in TALLYING state.
func (k Keeper) ValidateRoundForTally(ctx context.Context, roundID []byte) error {
	kvStore := k.OpenKVStore(ctx)
	round, err := k.GetVoteRound(kvStore, roundID)
	if err != nil {
		return err // wraps ErrRoundNotFound if missing
	}

	if round.Status != types.SessionStatus_SESSION_STATUS_TALLYING {
		return fmt.Errorf("%w: status is %s", types.ErrRoundNotTallying, round.Status)
	}

	return nil
}

// ValidateTallySubmitter checks that MsgSubmitTally is only submitted during
// block execution (not via mempool) and that the Creator matches the current
// block proposer. This ensures only the block proposer can inject tally txs
// (via PrepareProposal), preventing forged tally submissions.
func (k Keeper) ValidateTallySubmitter(ctx context.Context, creator string) error {
	sdkCtx := sdk.UnwrapSDKContext(ctx)

	// MsgSubmitTally must never enter the mempool — it can only be injected
	// by the block proposer via PrepareProposal.
	if sdkCtx.IsCheckTx() || sdkCtx.IsReCheckTx() {
		return fmt.Errorf("%w: MsgSubmitTally cannot be submitted via mempool", types.ErrInvalidField)
	}

	// During FinalizeBlock, verify Creator matches the block proposer.
	proposerConsAddr := sdk.ConsAddress(sdkCtx.BlockHeader().ProposerAddress)
	val, err := k.stakingKeeper.GetValidatorByConsAddr(ctx, proposerConsAddr)
	if err != nil {
		return fmt.Errorf("%w: failed to resolve block proposer: %v", types.ErrInvalidField, err)
	}
	if val.OperatorAddress != creator {
		return fmt.Errorf("%w: tally creator %s does not match block proposer %s",
			types.ErrInvalidField, creator, val.OperatorAddress)
	}
	return nil
}

// ValidateRoundActive checks that a vote round exists and has not expired.
// Deprecated: Use ValidateRoundForVoting or ValidateRoundForShares instead.
// Kept as a thin wrapper to minimize churn in existing callers.
func (k Keeper) ValidateRoundActive(ctx context.Context, roundID []byte) error {
	return k.ValidateRoundForVoting(ctx, roundID)
}

// CheckNullifiersUnique verifies that none of the provided nullifiers have
// already been recorded in the type-scoped, round-scoped nullifier set.
// This runs on every check including RecheckTx, because nullifiers may have
// been consumed by the newly committed block.
func (k Keeper) CheckNullifiersUnique(ctx context.Context, nfType types.NullifierType, roundID []byte, nullifiers [][]byte) error {
	kvStore := k.OpenKVStore(ctx)
	for _, nf := range nullifiers {
		has, err := k.HasNullifier(kvStore, nfType, roundID, nf)
		if err != nil {
			return err
		}
		if has {
			return fmt.Errorf("%w: %x", types.ErrDuplicateNullifier, nf)
		}
	}
	return nil
}

// putUint64BE writes a uint64 in big-endian byte order.
func putUint64BE(b []byte, v uint64) {
	b[0] = byte(v >> 56)
	b[1] = byte(v >> 48)
	b[2] = byte(v >> 40)
	b[3] = byte(v >> 32)
	b[4] = byte(v >> 24)
	b[5] = byte(v >> 16)
	b[6] = byte(v >> 8)
	b[7] = byte(v)
}

// getUint64BE reads a uint64 from big-endian bytes.
func getUint64BE(b []byte) uint64 {
	return uint64(b[0])<<56 | uint64(b[1])<<48 | uint64(b[2])<<40 | uint64(b[3])<<32 |
		uint64(b[4])<<24 | uint64(b[5])<<16 | uint64(b[6])<<8 | uint64(b[7])
}

